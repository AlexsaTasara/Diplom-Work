class TileMap:
    def __init__(self):
        self.map = []
        self.w = 0
        self.h = 0
        self.layer = 4

    def buildMapFromData(self, d, w, h):
        self.w = w
        self.h = h
        dd = len(d)
        ddd = len(d[0])
        if (dd != h) or (ddd != w):
            return False
        self.map = []
        for y in range(h):
            for x in range(w):
                self.map.append(Tile(x, y, d[y][x]))
        return True


